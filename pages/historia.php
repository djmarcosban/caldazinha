	<p style="text-align: justify;">A origem de Caldazinha teve início em meados de 1937, época em que sua região era conhecida como Milho-Inteiro. O município surgiu em terras doadas por Antônio Gouveia, Saturnino Rodrigues Siqueira, Macário Marques de Araújo e outros doadores. Conforme escrituras passadas no dia 16 de outubro de 1947 pela cúria de Goiás, as terras foram doadas à igreja em favor de Santa Nossa Senhora D'Abadia, padroeira de Caldazinha.
	O topônimo é devido à sua localização entre o Rio Caldas e o Ribeirão Sozinha.</p>
	<p style="text-align: justify;">Distrito criado com a denominação de Caldazinha, pela Lei Municipal n.º 175, de 10-11-1957, subordinado ao município de Bela Vista de Goiás. </p>
	<p style="text-align: justify;">Em divisão territorial datada de 1-VII-1960, o distrito de Caldazinha figura no município de Bela Vista de Goiás. Assim permanecendo em divisão territorial datada de 1-I-1979. </p>
	<p style="text-align: justify;">Elevado à categoria de município com a denominação de Caldazinha, pela Lei Estadual n.º 11.699, de 29-04-1992, desmembrado de Bela Vista de Goiás. Sede no antigo distrito de Caldazinha. Constituído do distrito sede. Instalado em 01-01-1993.</p>
	<p style="text-align: justify;">Em divisão territorial datada de 2001, o município é constituído do distrito sede.
	Assim permanecendo em divisão territorial datada de 2018.</p>
